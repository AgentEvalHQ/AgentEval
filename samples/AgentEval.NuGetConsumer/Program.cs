static void SafeClear()
{
    // Console.Clear() throws when input is redirected (piped)
    try
    {
        if (!Console.IsInputRedirected)
        {
            Console.Clear();
        }
    }
    catch (IOException)
    {
        // Ignore - running in non-interactive mode
    }
}
